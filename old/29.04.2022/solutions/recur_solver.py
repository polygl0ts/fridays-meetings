#! /usr/bin/python3
import time
flag = [0x76,0x71, 0xc5, 0xa9, 0xe2, 0x22, 0xd8, 0xb5, 0x73, 0xf1, 0x92, 0x28, 0xb2, 0xbf, 0x90, 0x5a, 0x76, 0x77, 0xfc, 0xa6, 0xb3, 0x21, 0x90, 0xda, 0x6f, 0xb5, 0xcf, 0x38]


rec_values = [3,5]



#0x1c => 28
for i in range(2,28*28):
	rec_values.append( 3*rec_values[i-2] + 2*rec_values[i-1])

l = 0
result = ""
while l < 0x1c :
	f = flag[l]
	r = rec_values[l*l]
	result += chr((r^f) & 0xFF)
	l+=1

print(result)